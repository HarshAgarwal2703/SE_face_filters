package com.gsrathoreniks.facefilter.GooglePlay;

class UserImageCords {

    private float x_cord;
    private float y_cord;

    public UserImageCords(float x_cord, float y_cord) {
        this.x_cord = x_cord;
        this.y_cord = y_cord;
    }

    public float getX_cord() {
        return x_cord;
    }

    public void setX_cord(float x_cord) {
        this.x_cord = x_cord;
    }

    public float getY_cord() {
        return y_cord;
    }

    public void setY_cord(float y_cord) {
        this.y_cord = y_cord;
    }
}
